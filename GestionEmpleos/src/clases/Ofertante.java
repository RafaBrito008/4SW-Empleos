public class Ofertante {

    private String nombre;
    private String apellido;
    private int cantidadEmpleosOfertados;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public int getCantidadEmpleosOfertados() {
        return cantidadEmpleosOfertados;
    }

    public void setCantidadEmpleosOfertados(int cantidadEmpleosOfertados) {
        this.cantidadEmpleosOfertados = cantidadEmpleosOfertados;
    }
   
    
    

}
